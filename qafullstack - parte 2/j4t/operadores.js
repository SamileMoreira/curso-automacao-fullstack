//operadores aritiméticos
 console.log(2+2);
 console.log(2-2);
 console.log(2*2);
 console.log(2/2);

//operadores de comparação

 console.log(2==2);
 console.log(2==3); //Retorna true porque os valores são iguais independente do tipo

console.log(2!=2);
console.log(2!=1);

//operador igual estrito (o valor e o tipo )
console.log(2 === "2");

//operador não igual estrito
console.log(2 !== "2");
console.log("2" !== "2");

console.log(2 > 3);
console.log(2 >= 2);
console.log(2 < 3);
console.log(3 <= 3);
